namespace BisqueWebHelper
{
    /// <summary>
    /// Simple class for managing bisque images.
    /// </summary>
    public struct BisqueImageResource
    {
        /// <summary>
        /// The name of the image.
        /// </summary>
        public string ImageName;

        /// <summary>
        /// The URI of the image.
        /// </summary>
        public string URI;

        /// <summary>
        /// The identifier of the image.
        /// </summary>
        public string Id;
    }
}
